// Biblioteca para entrada e saída de dados
#include <iostream>
using namespace std;

// Função principal
int main()
{
    // Inicializa a variável de controle do loop com 0
    int i = 0;

    // Loop while: executa enquanto i for menor que 10
    while (i < 10)
    {
        // Imprime o valor atual de i seguido de uma nova linha
        cout << i << endl;

        // Incrementa i em 1 a cada iteração
        i++;
    }

    // Retorna 0 indicando que o programa foi executado com sucesso
    return 0;
}
